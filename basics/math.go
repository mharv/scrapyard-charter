package basics

//Floats
type Vector2f struct {
	x float64
	y float64
}

type Vector3f struct {
	x float64
	y float64
	z float64
}

type FloatRect struct {
	x      float64
	y      float64
	width  float64
	height float64
}

//Ints
type Vector2i struct {
	x int
	y int
}

type Vector3i struct {
	x int
	y int
	z int
}

type IntRect struct {
	x      int
	y      int
	width  int
	height int
}
