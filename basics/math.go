package basics

//Floats
type Vector2f struct {
	X float64
	Y float64
}

type Vector3f struct {
	X float64
	Y float64
	Z float64
}

type FloatRect struct {
	X      float64
	Y      float64
	Width  float64
	Height float64
}

//Ints
type Vector2i struct {
	X int
	Y int
}

type Vector3i struct {
	X int
	Y int
	Z int
}

type IntRect struct {
	X      int
	Y      int
	Width  int
	Height int
}
